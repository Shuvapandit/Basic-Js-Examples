var lastseat=["Rahim","karim","shuva"];
//add
lastseat.push("sagor","kalam");
console.log(lastseat);
//remove
lastseat.pop();
console.log(lastseat);
//shift(): Remove an item from the beginning of an array
lastseat.shift();
console.log(lastseat);
//unshift() : Add items to the beginning of an array
lastseat.unshift("rahul");
console.log(lastseat);


